char	*ft_strrchr(const char *s, int c)
{
	char	*ans;

	c = c % 256;
	ans = 0;
	while (*s)
	{
		if (*s == c)
			ans = (char *)s;
		s++;
	}
	if (c == 0 && *s == 0)
		ans = (char *)s;
	return (ans);
}
