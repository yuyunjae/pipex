#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*uc_dst;
	unsigned char	*uc_src;

	uc_dst = (unsigned char *)dst;
	uc_src = (unsigned char *)src;
	i = 0;
	while (i < n && (uc_dst || uc_src))
	{
		uc_dst[i] = uc_src[i];
		i++;
	}
	return (dst);
}
