#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	num[11];
	int		i;

	i = 0;
	if (n < 0)
		write(fd, "-", 1);
	else if (n == 0)
		write(fd, "0", 1);
	while (n > 0)
	{
		num[i++] = '0' + n % 10;
		n /= 10;
	}
	while (n < 0)
	{
		num[i++] = '0' - n % 10;
		n /= 10;
	}
	while (i-- > 0)
	{
		write(fd, &num[i], 1);
	}
}
